<script>
    var productos=[];
    var valores=[];
    $(document).ready(function(){
        $.ajax({
            url:'/productos',
            method:'GET',
        }).done(function(res){
            var arreglo = JSON.parse(res);
            for (let index = 0; index < arreglo.length; index++) {
                console.log(arreglo);
                productos.push(arreglo[index].descripcion);
                valores.push(arreglo[index].cantidad)
                console.log(productos);
            }
        });
    });
    var ctx = document.getElementById('myChart').getContext('2d');
    var myChart = new Chart(ctx, {
        type: 'bar',
        data: {
            labels: productos,
            datasets: [{
                label: 'Control de productos',
                data: valores,
                backgroundColor: [
                    'rgba(255, 99, 132, 0.2)',
                    'rgba(54, 162, 235, 0.2)',
                    'rgba(255, 206, 86, 0.2)',
                    'rgba(75, 192, 192, 0.2)',
                    'rgba(153, 102, 255, 0.2)',
                    'rgba(255, 159, 64, 0.2)'
                ],
                borderColor: [
                    'rgba(255, 99, 132, 1)',
                    'rgba(54, 162, 235, 1)',
                    'rgba(255, 206, 86, 1)',
                    'rgba(75, 192, 192, 1)',
                    'rgba(153, 102, 255, 1)',
                    'rgba(255, 159, 64, 1)'
                ],
                borderWidth: 1
            }]
        },
        options: {
            scales: {
                yAxes: [{
                    ticks: {
                        beginAtZero: true
                    }
                }]
            }
        }
    });
    </script>

